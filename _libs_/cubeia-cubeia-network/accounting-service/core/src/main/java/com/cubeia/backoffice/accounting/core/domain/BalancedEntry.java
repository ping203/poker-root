package com.cubeia.backoffice.accounting.core.domain;

import java.math.BigDecimal;

import org.apache.commons.lang.builder.EqualsBuilder;
import org.apache.commons.lang.builder.HashCodeBuilder;
import org.apache.commons.lang.builder.ToStringBuilder;
import org.apache.commons.lang.builder.ToStringStyle;

import com.cubeia.backoffice.accounting.core.entity.Entry;

public class BalancedEntry extends Entry {

	private static final long serialVersionUID = -7045325508987519723L;
	
	private BigDecimal balance;

	protected BalancedEntry() { }
	
	public BalancedEntry(Entry parent, BigDecimal balance) {
		super.setAccount(parent.getAccount());
		super.setAmount(parent.getAmount());
		super.setId(parent.getId());
		super.setTransaction(parent.getTransaction());
		this.balance = balance;
	}
	
	public BigDecimal getBalance() {
		return balance;
	}
	
	@Override
	public String toString() {
		return ToStringBuilder.reflectionToString(this, ToStringStyle.SHORT_PREFIX_STYLE);
	}
	
	@Override
	public boolean equals(Object obj) {
		return EqualsBuilder.reflectionEquals(this, obj);
	}
	
	@Override
	public int hashCode() {
		return HashCodeBuilder.reflectionHashCode(this);
	}
}
