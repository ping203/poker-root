package com.cubeia.backoffice.operator.api;

import java.io.Serializable;

import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement(name = "operator")
public class OperatorDTO implements Serializable {

    private static final long serialVersionUID = 2153378632814990027L;

    private long id;
    private String name;
    private boolean enabled;
    private OperatorAccountStatus accountStatus;
    
    public OperatorDTO(){
    }

    @XmlElement
    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    @XmlElement
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @XmlElement
    public OperatorAccountStatus getAccountStatus() {
		return accountStatus;
	}

	public void setAccountStatus(OperatorAccountStatus accountStatus) {
		this.accountStatus = accountStatus;
	}

	@XmlElement
    public boolean isEnabled() {
        return enabled;
    }

    public void setEnabled(boolean enabled) {
        this.enabled = enabled;
    }

    @Override
    public String toString() {
        return "OperatorDTO{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", enabled=" + enabled +
                '}';
    }
}